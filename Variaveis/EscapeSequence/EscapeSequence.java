public class EscapeSequence {
	
/*
	Existem várias outras sequências de escape, sendo que as mais conhecidas são:
	- \n para nova linha
	- \\ para uma barra invertida
	- \" para aspas duplas
*/
	public static void main(String []args){
		System.out.println("Oi \"Maria\""); //Aspas duplas
		System.out.println("Oi \n Maria"); //nova linha
		System.out.println("Oi \\ Maria"); //Barra Invertida

	}
}